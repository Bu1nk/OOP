package ru.nsu.abramkin.model;

/**
 * Перечисление направлений движения змейки.
 * Используется для задания текущего направления и управления вводом игрока.
 */
public enum Direction {
    /** Движение вверх (уменьшение Y). */
    UP,

    /** Движение вниз (увеличение Y). */
    DOWN,

    /** Движение влево (уменьшение X). */
    LEFT,

    /** Движение вправо (увеличение X). */
    RIGHT;
}
